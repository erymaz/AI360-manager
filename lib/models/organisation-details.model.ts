export function getLocalizedCountries(locale: string): Array<LocalizedCountry> {
  return COUNTRIES.map((country) => {
    let name: string;
    switch (locale) {
      case "en-US": {
        name = country.en;
        break;
      }
      case "de-DE": {
        name = country.de;
        break;
      }
      case "es-ES": {
        name = country.es;
        break;
      }
      default: {
        name = country.en;
      }
    }
    return { name, flag: country.flag, iso2: country.iso2 };
  }).sort((c1, c2) => c1.name.localeCompare(c2.name, locale));
}

